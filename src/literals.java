
public class literals {

    public static void main(String[] args) {
        int em = 0b01;
        System.out.println(em);

        int ws = 0x734;
        System.out.println(ws);

        int uy = 100_000;
        System.out.println(uy);

        double num1 = 34;
        System.out.println(num1);

        double nu = 12e31;
        System.out.println(nu);

        char x = 'a';
        x++;
        System.out.println(x);
    }
}