public class typecasting {
    public static void main(String[] args) {
        byte b = 127;
        int a = b;
        System.out.println(a);

        int c = 894;
        byte p = (byte) c;
        System.out.println(p);

        float f = 89.98f;
        int o = (int) f;
        System.out.println(o);
    }
}
