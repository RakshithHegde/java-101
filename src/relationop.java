public class relationop {
    public static void main(String[] args) {
        int r = 9;
        int y = 8;

        boolean result = r <= y;
        boolean r1 = r >= y;
        boolean r2 = r == y;

        System.out.println(result);
        System.out.println(r1);
        System.out.println(r2);

        // also works with other data types
    }
}
